package ru.job4j.calculator;

public class Calculator {

    /**
     * Method add
     * @param first input number
     * @param second input number
     * @return first + second
     */

    public double add(double first, double second) {
        return first + second;
    }

    /**
     * Method subtract
     * @param first input number
     * @param second input number
     * @return first - second
     */
	 public double subtract(double first, double second) {
        return first - second;
    }
    /**
     * Method multiply
     * @param first input number
     * @param second input number
     * @return first * second
     */
	 public double multiply(double first, double second) {
        return first * second;
    }
    /**
     * Method divide
     * @param first input number
     * @param second input number
     * @return first / second
     */
	 public double divide(double first, double second) {
        return first / second;
    }

}